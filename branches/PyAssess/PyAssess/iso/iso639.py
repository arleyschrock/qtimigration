ISO639Ranges=[]

# The type table and corresponding code constants
ISO639Type={}
ISO639UnknownCode=0
ISO639BibliographicCode=1
ISO639TerminologicCode=2
ISO639Alpha2Code=3
ISO639ReservedCode=4

ISO639Bib={}
ISO639Term={}
ISO639Alpha2={}
ISO639EnglishName={}
ISO639FrenchName={}

def BuildTables ():
	lines=ISO639_2_VALUES.splitlines()
	for line in lines:
		fields=line.split('|')
		bibCode=fields[0].strip()
		termCode=fields[1].strip()
		alpha2Code=fields[2].strip()
		if "-" in bibCode:
			# We have a range
			rcodes=bibCode.split('-')
			assert (rcodes[0]<rcodes[1] and not (termCode or alpha2Code))
			ISO639Ranges.append((rcodes[0],rcodes[1]))
			bibCode=rcodes[0]
		else:
			if termCode:
				ISO639Type[termCode]=ISO639TerminologicCode
				if alpha2Code:
					ISO639Alpha2[termCode]=alpha2Code
					ISO639Term[alpha2Code]=termCode
				ISO639Term[bibCode]=termCode
				ISO639Bib[termCode]=bibCode
				ISO639Term[termCode]=termCode
			if alpha2Code:
				ISO639Type[alpha2Code]=ISO639Alpha2Code
				ISO639Alpha2[bibCode]=alpha2Code
				ISO639Bib[alpha2Code]=bibCode
				ISO639Alpha2[alpha2Code]=alpha2Code
			ISO639Type[bibCode]=ISO639BibliographicCode
			ISO639Bib[bibCode]=bibCode
		ISO639EnglishName[bibCode]=unicode(fields[3],"iso8859-1")
		ISO639FrenchName[bibCode]=unicode(fields[4],"iso8859-1")

def GetISO639CodeType(code):
	code=code.lower()
	ctype=ISO639Type.get(code,None)
	if not ctype:
		# Test the ranges
		for r in ISO639Ranges:
			if code>=r[0] and code<=r[1]:
				ctype=ISO639ReservedCode
				break
	if not ctype:
		ctype=ISO639UnknownCode
	return ctype
		
def GetISO639Alpha2Code(code):
	return ISO639Alpha2.get(code.lower(),None)

def GetISO639BibCode(code):
	# Bibliographic code
	code=code.lower()
	bcode=ISO639Bib.get(code,None)
	if not bcode and len(code)==3:
		# Test the ranges
		for r in ISO639Ranges:
			if code>=r[0] and code<=r[1]:
				bcode=code
				break
	return bcode

def GetISO639TermCode(code):
	# Terminolgic code
	return ISO639Term.get(code.lower(),None)

def GetISO639CanonicalCode(code):
	# Alpha2 code, terminologic or ultimately bibliographic 
	ccode=GetISO639Alpha2Code(code)
	if not ccode:
		ccode=GetISO639TermCode(code)
	if not ccode:
		ccode=GetISO639BibCode(code)
	return ccode

def GetISO639NameCode(code):
	# Differs slightly from GetISO639BibCode because special codes are
	# replaced by lowest bib-code in the special range
	code=code.lower()
	ncode=ISO639Bib.get(code,None)
	if not ncode and len(code)==3:
		# Test the ranges
		for r in ISO639Ranges:
			if code>=r[0] and code<=r[1]:
				ncode=r[0]
				break
	return ncode
	
def GetISO639EnglishName(code):
	return ISO639EnglishName.get(GetISO639NameCode(code),None)
	
def GetISO639FrenchName(code):
	return ISO639FrenchName.get(GetISO639NameCode(code),None)


